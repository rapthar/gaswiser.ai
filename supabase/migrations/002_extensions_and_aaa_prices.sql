-- Enable PostGIS-independent distance extensions
CREATE EXTENSION IF NOT EXISTS cube;
CREATE EXTENSION IF NOT EXISTS earthdistance;

-- ─── AAA Gas Prices table (state-level averages from AAA) ────────────────────
CREATE TABLE IF NOT EXISTS aaa_gas_prices (
  id                   UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  state_code           CHAR(2) NOT NULL UNIQUE,
  state_name           VARCHAR(100) NOT NULL,
  regular_price        NUMERIC(5,3),
  mid_grade_price      NUMERIC(5,3),
  premium_price        NUMERIC(5,3),
  diesel_price         NUMERIC(5,3),
  current_avg_data     JSONB,
  yesterday_avg_data   JSONB,
  week_ago_avg_data    JSONB,
  month_ago_avg_data   JSONB,
  year_ago_avg_data    JSONB,
  updated_at           TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

